#include "libft.h"

/*
** ft_putendl_fd - Écrit une chaîne suivie d'un newline
** @s: chaîne à écrire
** @fd: file descriptor
*/
void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
		return ;
	ft_putstr_fd(s, fd);
	write(fd, "\n", 1);
}
