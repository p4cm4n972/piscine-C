#include "libft.h"

/*
** ft_lstlast - Retourne le dernier maillon
** @lst: tÃªte de la liste
** Return: dernier maillon, NULL si liste vide
*/
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
