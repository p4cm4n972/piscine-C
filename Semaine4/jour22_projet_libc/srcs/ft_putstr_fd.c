#include "libft.h"

/*
** ft_putstr_fd - Écrit une chaîne sur un file descriptor
** @s: chaîne à écrire
** @fd: file descriptor
*/
void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
		return ;
	write(fd, s, ft_strlen(s));
}
