#include "libft.h"

/*
** ft_strlen - Calcule la longueur d'une chaîne
** @s: chaîne à mesurer
** Return: nombre de caractères avant le '\0'
*/
size_t	ft_strlen(const char *s)
{
	size_t	len;

	len = 0;
	while (s[len])
		len++;
	return (len);
}
