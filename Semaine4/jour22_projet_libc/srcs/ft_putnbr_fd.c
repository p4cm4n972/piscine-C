#include "libft.h"

/*
** ft_putnbr_fd - Écrit un entier sur un file descriptor
** @n: entier à écrire
** @fd: file descriptor
**
** Gère INT_MIN correctement
*/
void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd('0' + n % 10, fd);
}
