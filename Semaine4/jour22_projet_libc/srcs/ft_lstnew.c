#include "libft.h"

/*
** ft_lstnew - Crée un nouveau maillon de liste
** @content: contenu du maillon (void* pour généricité)
** Return: nouveau maillon, NULL si échec malloc
*/
t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}
