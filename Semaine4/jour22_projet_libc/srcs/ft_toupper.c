#include "libft.h"

/*
** ft_toupper - Convertit une minuscule en majuscule
** @c: caractère à convertir
** Return: majuscule si c était minuscule, c sinon
*/
int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	return (c);
}
