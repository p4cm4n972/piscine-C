#include "libft.h"

/*
** ft_putchar_fd - Écrit un caractère sur un file descriptor
** @c: caractère à écrire
** @fd: file descriptor (0=stdin, 1=stdout, 2=stderr)
*/
void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}
