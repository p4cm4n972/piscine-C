#include "libft.h"

/*
** ft_isascii - Vérifie si c est dans la table ASCII (0-127)
** @c: caractère à tester
** Return: non-zéro si ASCII, 0 sinon
*/
int	ft_isascii(int c)
{
	return (c >= 0 && c <= 127);
}
