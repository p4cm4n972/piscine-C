#include "libft.h"

/*
** ft_lstadd_back - Ajoute un maillon à la fin de la liste
** @lst: pointeur vers le pointeur de tête
** @new: maillon à ajouter
*/
void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!lst || !new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(*lst);
	last->next = new;
}
