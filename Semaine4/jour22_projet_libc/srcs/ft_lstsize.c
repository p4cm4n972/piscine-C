#include "libft.h"

/*
** ft_lstsize - Compte le nombre de maillons
** @lst: tÃªte de la liste
** Return: nombre de maillons
*/
int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	while (lst)
	{
		count++;
		lst = lst->next;
	}
	return (count);
}
